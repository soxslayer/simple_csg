Math::Vector3 MeshVisualization::edge_intersection (const Math::Vector3& pt1,
                                                    const Math::Vector3& pt2,
                                                    double d1, double d2)
{
  Math::Vector3 r = pt1;
  double m = d1 / (d1 - d2);

  r[0] += m * (pt2[0] - pt1[0]);
  r[1] += m * (pt2[1] - pt1[1]);
  r[2] += m * (pt2[2] - pt1[2]);

  return r;
}

